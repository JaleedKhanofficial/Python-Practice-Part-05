# Write a program that calculates and prints the sum of all the values in a dictionary. Assume the values are numbers.

print("** Sum of All Values **")

# Define the dictionary
my_dict = {'a': 10, 'b': 20, 'c': 30}

# Calculate the sum of all values
value_sum = sum(my_dict.values())

# Print the sum
print("Sum of all values:", value_sum)

