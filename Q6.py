# Write a program that defines a dictionary and uses a loop to print each key and its corresponding value in the format:
# key -> value.

print("** Print Keys and Values Together **")

# Define the dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}

# Print each key and its corresponding value
print("Keys and values in the dictionary:")
for key, value in my_dict.items():
    print(f"{key} -> {value}")

