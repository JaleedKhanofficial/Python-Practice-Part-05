# Write a program to create an empty dictionary, add a new key-value pair provided by the user, and then print the updated dictionary. 

print("** Add and Retrieve a Key-Value Pair **")

# Create an empty dictionary
my_dict = {}

# Get key-value pair from the user
key = input("Enter a key: ")
value = input("Enter a value: ")

# Add the key-value pair to the dictionary
my_dict[key] = value

# Print the updated dictionary
print("Updated Dictionary:", my_dict)

