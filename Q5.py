# Write a program that checks if a specific key (e.g., "name") exists in a dictionary and prints an appropriate message.

print("** Check Membership in Keys **")

# Define the dictionary
my_dict = {'name': 'Alice', 'age': 25, 'city': 'New York'}

# Check if the key 'name' exists
if 'name' in my_dict:
    print("Key 'name' is present in the dictionary.")
else:
    print("Key 'name' is not found.")

